load_data <- function(){

  # read the data from file and save it to a data frame.
  filename <- "household_power_consumption.txt"
  df<-read.csv(filename, sep=";", header=TRUE,stringsAsFactors=FALSE,na.strings="?")
 
  # convert the time and date to their classes
  df$Time <- strptime(paste(df$Date, df$Time), "%d/%m/%Y %H:%M:%S") 
  df$Date <- as.Date(df$Date, "%d/%m/%Y") 

  # extract the required data
  dates_extract <- as.Date(c("2007-02-01", "2007-02-02"), "%Y-%m-%d")
  df <- subset(df, Date %in% dates_extract)

  # run str(df) to learn the data
  #str(df)
